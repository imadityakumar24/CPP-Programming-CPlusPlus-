// relational operators

#include <iostream>
using namespace std;
int main() {

    int a = 10, b = 20;
    
    cout << " a < b = " << (a < b) << endl;
    cout << " (a + 4) > (b+ 5) = " << ((a + 4) > (b+ 5)) << endl;
    cout << " a > b = " << (a > b) << endl;
    cout << " a <= b = " << (a <= b) << endl;
    cout << " a >= b = " << (a >= b) << endl;
    cout << " a == b = " << (a == b) << endl;
    cout << " a != b = " << (a != b) << endl;
   
    return 0;
    
}