// typecasting
// means changing the data type




#include <iostream>
using namespace std ;

int main(){

    float x = 234.628;
    int  y = 34; 
    
    cout << x << endl;
    cout << (int)x << endl;
    // both are same 
    cout << int(x) << endl;
    cout << y << endl;
    cout << (float)y << endl;
    
    
    return 0;
    
}